package ru.job4j.loop;

public class MultiplicationLoop {
    @SuppressWarnings("checkstyle:WhitespaceAfter")
    public static int mult(int a, int b) {
        int i = 0;
        int rsl = 0;
        for (i = a; i < b; i++) {
           //* int rsl;
          if (i == (int) i) {
              rsl = i * i;
            }
        }
        i = rsl;
        return i; }
         }

